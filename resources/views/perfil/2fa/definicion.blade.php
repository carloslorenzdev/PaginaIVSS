<div class="space-y-3">
    <h3 class="text-2xl">¿Qué es la Autenticación de Dos Factores?</h3>
    <p class="text-base text-gray-800 dark:text-neutral-200 leading-7">
        La autenticación de dos factores (2FA) es un sistema de seguridad que requiere dos métodos de verificación
        de identidad para acceder a una cuenta o sistema. Esto añade una capa adicional de protección más allá de la
        contraseña, dificultando el acceso no autorizado incluso si la contraseña es robada.
    </p>
</div>
